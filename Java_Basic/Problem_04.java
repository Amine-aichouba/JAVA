public class Problem_04 
{
    public static void main ( String[] args ) 
    {
        int a = -5 + 8 * 6 ;
        int b = ( 55 + 9 ) % 9 ; 
        int c = 20 + -3 * 5 / 8 ;
        int d = 5 + 15 / 3 * 2 - 8 % 3 ;

        System.out.printf ( "\n\n a : -5 + 8 * 6 = %d " , a ) ;
        System.out.printf ( "\n\n b : ( 55 + 9 ) %% 9 = %d " , b ) ;   
        System.out.printf ( "\n\n c : 20 + (-3) * 5 / 8 = %d " , c ) ;
        System.out.printf ( "\n\n d : 5 + 15 / 3 * 2 - 8 %% 3 = %d \n\n\n" , d ) ; 
    }
}
